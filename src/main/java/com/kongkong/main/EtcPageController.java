package com.kongkong.main;

import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;

/**
 * Handles requests for the application home page.
 */
@Controller
public class EtcPageController {
	@RequestMapping("/etc/introduce")
	public String introduce() {
		return "etc/introduce";
	}
	@RequestMapping("/etc/business")
	public String business() {
		return "etc/business";
	}
	@RequestMapping("/etc/partnership")
	public String partnership() {
		return "etc/partnership";
	}
	@RequestMapping("/etc/recruitment")
	public String recruitment() {
		return "etc/recruitment";
	}
	@RequestMapping("/etc/conditions")
	public String conditions() {
		return "etc/conditions";
	}
	@RequestMapping("/etc/policy")
	public String policy() {
		return "etc/policy";
	}
	@RequestMapping("/etc/adolescentProtection")
	public String adolescentProtection() {
		return "etc/adolescentProtection";
	}
	@RequestMapping("/etc/personalInformation")
	public String personalInformation() {
		return "etc/personalInformation";
	}
	@RequestMapping("/etc/customerService")
	public String customerService() {
		return "etc/customerService";
	}
}
